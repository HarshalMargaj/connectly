import { QueryClient } from "@tanstack/react-query";

export const createQueryClient = () =>
	new QueryClient({
		defaultOptions: {
			queries: {
				staleTime: 5 * 60 * 1000,
				gcTime: 10 * 60 * 1000,
				refetchOnWindowFocus: false,
				refetchOnReconnect: true,
				retry: 1,
			},
			mutations: {
				retry: 0,
			},
		},
	});
